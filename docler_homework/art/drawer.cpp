nSystem::Error Drawer::Draw( nSystem::IOutput& pOutput )
{
    const nSystem::Error res = pOutput.SetColor( kRedBackground, kWhiteForeground );
    if ( !res )
    {
        return res;
    }

    for ( const sData& data : kCoords )
    {
        assert( data.text != nullptr );
        const nSystem::Error moveRes = pOutput.MoveCursor( data.coord );
        if ( !moveRes )
        {
            return moveRes;
        }

        const nSystem::Error putRes = pOutput.PutString( "%s", data.text );
        if ( !putRes )
        {
            return putRes;
        }
    }

    return nSystem::Error();
}
